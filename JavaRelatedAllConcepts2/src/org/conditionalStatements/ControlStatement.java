package org.conditionalStatements;

public class ControlStatement{
	public static void main(String[] args){
		int a = 0;

		switch(a){
		case 5 :
			System.out.println("Given value is 5");
			break;
		case 8 :
			System.out.println("Given value is 8");
		case 10 :
			System.out.println("Given value is 10");
			break;
		case 15 :
			System.out.println("Given value is 15");
		case 20 :
			System.out.println("Given value is 20");
		default :
			System.out.println("Given value is 0");

		}
}
}
