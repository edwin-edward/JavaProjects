package org.javaInterviewQuestions;
import java.util.TreeSet;

public class HowToSortTheString{
	public static void main(String[] args){
		TreeSet set = new TreeSet();

		set.add("Cat");
		set.add("Apple");
		set.add("Eat");
		set.add("Ball");
		set.add("Dog");

	System.out.println(set);

	for(Object ss : set){
		System.out.println(ss);
	}
}
}

